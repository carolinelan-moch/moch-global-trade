async function detectPreferredLanguage() {
    const savedLanguage = localStorage.getItem("siteLanguage");

    if (savedLanguage === "zh" || savedLanguage === "en") {
        return savedLanguage;
    }

    const browserLanguages = navigator.languages || [
        navigator.language || navigator.userLanguage || ""
    ];

    const hasChineseBrowser = browserLanguages.some((lang) =>
        String(lang).toLowerCase().startsWith("zh")
    );

    if (hasChineseBrowser) {
        return "zh";
    }

    try {
        const response = await fetch("https://ipapi.co/json/", {
            method: "GET",
            cache: "no-store"
        });

        if (!response.ok) {
            throw new Error("Geo IP request failed");
        }

        const data = await response.json();

        if (data && data.country_code === "CN") {
            return "zh";
        }
    } catch (error) {
        console.warn("IP location check failed. Falling back to English.", error);
    }

    return "en";
}

function renderSlide() {
    const slide = slides[currentLang][currentSlide];

    heroSection.style.setProperty("--hero-bg", `url('${slide.bg}')`);
    heroEyebrow.textContent = slide.eyebrow;
    heroTitle.textContent = slide.title;
    heroDesc.textContent = slide.desc;
    heroMeta1.textContent = slide.meta1;
    heroMeta2.textContent = slide.meta2;

    dots.innerHTML = "";

    slides[currentLang].forEach((_, index) => {
        const button = document.createElement("button");

        button.className = index === currentSlide ? "active" : "";
        button.setAttribute("aria-label", `Slide ${index + 1}`);

        button.addEventListener("click", () => {
            currentSlide = index;
            renderSlide();
            restartTimer();
        });

        dots.appendChild(button);
    });
}

function nextSlide() {
    currentSlide = (currentSlide + 1) % slides[currentLang].length;
    renderSlide();
}

function prevSlide() {
    currentSlide =
        (currentSlide - 1 + slides[currentLang].length) %
        slides[currentLang].length;

    renderSlide();
}

function restartTimer() {
    clearInterval(timer);
    timer = setInterval(nextSlide, 30000);
}

function setLang(lang) {
    currentLang = lang;
    html.lang = lang === "zh" ? "zh-CN" : "en";

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;

        if (i18n[lang][key]) {
            element.textContent = i18n[lang][key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.dataset.i18nPlaceholder;

        if (i18n[lang][key]) {
            element.placeholder = i18n[lang][key];
        }
    });

    zhBtn.classList.toggle("active", lang === "zh");
    enBtn.classList.toggle("active", lang === "en");

    document.title =
        lang === "zh"
            ? "MOCH GLOBAL TRADE LIMITED | 公司主页"
            : "MOCH GLOBAL TRADE LIMITED | Company Homepage";

    localStorage.setItem("siteLanguage", lang);

    currentSlide = 0;
    renderSlide();
    restartTimer();
}

zhBtn.addEventListener("click", () => setLang("zh"));
